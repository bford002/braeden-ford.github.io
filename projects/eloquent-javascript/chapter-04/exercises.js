function range(start, end, step) {
  var range = [];
  if (start === end) {
    return [];
  }
  if (step === undefined) {
    for (var i = start; i <= end; i++) {
      range.push(i);
    }
  } else {
    if (step < 0) {
      return [];
    } else {
      for (var i = start; i <= end; i+=step) {
        range.push(i);
      }
    }
  }
  return range;
}

